package minbft

import (
	"fmt"

	"github.com/hyperledger-labs/minbft/api"
	"github.com/hyperledger-labs/minbft/core/internal/peerstate"
	"github.com/hyperledger-labs/minbft/messages"
	"github.com/hyperledger-labs/minbft/usig"
)

// uiCapturer verifies USIG unique identifier of message, and
// synchronizes beginning of sequential processing of it.
//
// Processing of messages with UI generated by the same replica is
// synchronized by waiting to ensure each of the messages is processed
// only once and in the order of UI assigned. The return value new
// indicates if the message needs to be processed. The processing has
// to be completed by corresponding uiReleaser invoked on the message.
// It is safe to invoke concurrently.
type uiCapturer func(msg messages.MessageWithUI) (new bool, err error)

// uiReleaser synchronizes completion of sequential processing of
// messages with USIG unique identifier attached.
//
// It allows to continue sequential processing of messages with UI,
// synchronized by uiCapturer. It is safe to invoke concurrently.
type uiReleaser func(msg messages.MessageWithUI)

// uiVerifier verifies USIG certificate attached to a message.
//
// USIG certificate is verified and the UI is returned if it is valid
// for the message. A UI with zero counter value is never valid.
type uiVerifier func(msg messages.MessageWithUI) (ui *usig.UI, err error)

// uiAssigner assigns a unique identifier to a message.
//
// USIG UI is assigned and attached to the supplied message.
type uiAssigner func(msg messages.MessageWithUI)

// makeUICapturer constructs uiCapturer using the supplied interfaces.
func makeUICapturer(providePeerState peerstate.Provider, verifyUI uiVerifier) uiCapturer {
	return func(msg messages.MessageWithUI) (new bool, err error) {
		ui, err := verifyUI(msg)
		if err != nil {
			return false, err
		}

		peerState := providePeerState(msg.ReplicaID())

		return peerState.CaptureUI(ui), nil
	}
}

// makeUIReleaser constructs uiReleaser using the supplied peer state
// provider interface.
func makeUIReleaser(providePeerState peerstate.Provider) uiReleaser {
	return func(msg messages.MessageWithUI) {
		peerState := providePeerState(msg.ReplicaID())

		ui := new(usig.UI)
		if err := ui.UnmarshalBinary(msg.UIBytes()); err != nil {
			panic(fmt.Errorf("Failed unmarshaling UI: %s", err))
		}

		if err := peerState.ReleaseUI(ui); err != nil {
			panic(err)
		}
	}
}

// makeUIVerifier constructs uiVerifier using the supplied external
// authenticator to verify USIG certificates.
func makeUIVerifier(authen api.Authenticator) uiVerifier {
	return func(msg messages.MessageWithUI) (*usig.UI, error) {
		ui := new(usig.UI)

		if err := ui.UnmarshalBinary(msg.UIBytes()); err != nil {
			return nil, fmt.Errorf("Failed unmarshaling UI: %s", err)
		} else if ui.Counter == uint64(0) {
			return nil, fmt.Errorf("Invalid (zero) counter value")
		}

		if err := authen.VerifyMessageAuthenTag(api.USIGAuthen, msg.ReplicaID(), msg.Payload(), msg.UIBytes()); err != nil {
			return nil, fmt.Errorf("Failed verifying USIG certificate: %s", err)
		}

		return ui, nil
	}
}

// makeUIAssigner constructs uiAssigner using the supplied external
// authentication interface to generate USIG UIs.
func makeUIAssigner(authen api.Authenticator) uiAssigner {
	return func(msg messages.MessageWithUI) {
		uiBytes, err := authen.GenerateMessageAuthenTag(api.USIGAuthen, msg.Payload())
		if err != nil {
			panic(err)
		}

		msg.AttachUI(uiBytes)
	}
}
